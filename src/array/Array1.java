package array;

public class Array1 {
    public static void main(String[] args) {
        int[] students;  //배열 선언
        students = new int[5]; //int가 5개 있는 그릇.

        //변수값 대입
        students[0] = 90;
        students[1] = 80;
        students[2] = 70;
        students[3] = 60;
        students[4] = 50;

        System.out.println(students[0]);
    }
}
